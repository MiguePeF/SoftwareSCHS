    public GestionAlumnos() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        try {
             PreparedStatement ps=cn.prepareStatement("SELECT * FROM alumno;");
             ResultSet rs=ps.executeQuery();
             
             tabla_gestion_alumnos = new JTable (model);
             jScrollPanel.setViewportView(tabla_gestion_alumnos);
             
             model.addColumn("id");
             model.addColumn("Nombre");
             model.addColumn("Apellido Paterno");
             model.addColumn("Apellido Materno");
             model.addColumn("Semestre");
             model.addColumn("NIA");
             
             while(rs.next()){
                 Object[] fila=new Object[6];
                 for (int i=0; i<6; i++){
                     fila[i]=rs.getObject(i+i);
                 }
                 model.addRow(fila);
                 
             }
             cn.close();
             
            
        } catch (SQLException e){
           System.err.print("Error en el llenado de la tabla");
            
        }
        
        tabla_gestion_alumnos.addMouseListener(new MouseAdapter(){
        
        @Override
        public void mouseClicked(MouseEvent e){
            int fila_point=tabla_gestion_alumnos.rowAtPoint(e.getPoint());
            int columna_point=0;
            
            if(fila_point>=1){
                idalumno=(int)model.getValueAt(fila_point, columna_point);
                Alumnos alumnos=new Alumnos();
                alumnos.setVisible(true);
                dispose();
            }
            
        }
    });//////////////////////////
    }
