    public GestionMaterias() {
        initComponents();
        
        this.setLocationRelativeTo(null);
        cerrar1();
        
       try {
             PreparedStatement ps=cn.prepareStatement("SELECT * FROM materia");
             ResultSet rs=ps.executeQuery();
             
             tabla_gestion_materias = new JTable (modelo);
             jScrollPane1.setViewportView(tabla_gestion_materias);
             
             modelo.addColumn("id");
             modelo.addColumn("Materia");
             
             while(rs.next()){
                 Object[] fila=new Object[2];
                 for (int i=0; i<2; i++){
                     fila[i]=rs.getObject(i+i);
                 }
                 modelo.addRow(fila);
                 
             }
             cn.close();
             
            
        } catch (SQLException e){
           System.err.print("Error en el llenado de la tabla Materia");
            
        }
    }
   
    
